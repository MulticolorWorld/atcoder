fun main(args: Array<String>) {
    val c = Array(3) { readLine()!!.split(" ").map { it.toInt() }.toIntArray() }
    val f = c[0][1] - c[0][0] == c[1][1] - c[1][0] && c[1][1] - c[1][0] == c[2][1] - c[2][0] &&
            c[0][2] - c[0][1] == c[1][2] - c[1][1] && c[1][2] - c[1][1] == c[2][2] - c[2][1] &&
            c[0][0] - c[0][2] == c[1][0] - c[1][2] && c[1][0] - c[1][2] == c[2][0] - c[2][2] &&
            c[1][0] - c[0][0] == c[1][1] - c[0][1] && c[1][1] - c[0][1] == c[1][2] - c[0][2] &&
            c[2][0] - c[1][0] == c[2][1] - c[1][1] && c[2][1] - c[1][1] == c[2][2] - c[1][2] &&
            c[0][0] - c[2][0] == c[0][1] - c[2][1] && c[0][1] - c[2][1] == c[0][2] - c[2][2]
    println(if (f) "Yes" else "No")
}